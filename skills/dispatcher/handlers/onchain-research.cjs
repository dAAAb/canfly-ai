/**
 * Onchain Research Report handler
 *
 * Analyzes on-chain data via Base RPC and generates a research report.
 *
 * Params:
 *   address    - Wallet or contract address to analyze (required)
 *   chain      - Chain: "base", "ethereum" (default: "base")
 *   depth      - Analysis depth: "quick", "standard", "deep" (default: "standard")
 *   outDir     - Output directory (default: /tmp/littlelobster)
 */

const fs = require('fs');
const path = require('path');
const https = require('https');
const http = require('http');

const OPENAI_API_KEY = process.env.OPENAI_API_KEY;

const RPC_URLS = {
  base: 'https://mainnet.base.org',
  ethereum: 'https://eth.llamarpc.com',
};

// --- HTTP helpers -------------------------------------------------------------

function jsonRequest(url, body, headers = {}) {
  return new Promise((resolve, reject) => {
    const parsed = new URL(url);
    const mod = parsed.protocol === 'https:' ? https : http;
    const payload = JSON.stringify(body);

    const req = mod.request(parsed, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(payload),
        ...headers,
      },
    }, (res) => {
      const chunks = [];
      res.on('data', c => chunks.push(c));
      res.on('end', () => {
        const raw = Buffer.concat(chunks).toString();
        try { resolve({ status: res.statusCode, data: JSON.parse(raw) }); }
        catch { resolve({ status: res.statusCode, data: raw }); }
      });
    });
    req.on('error', reject);
    req.write(payload);
    req.end();
  });
}

// --- RPC calls ----------------------------------------------------------------

async function rpcCall(chain, method, params) {
  const url = RPC_URLS[chain];
  if (!url) throw new Error(`Unsupported chain: ${chain}`);

  const res = await jsonRequest(url, {
    jsonrpc: '2.0',
    id: 1,
    method,
    params,
  });

  if (res.data?.error) {
    throw new Error(`RPC error: ${JSON.stringify(res.data.error)}`);
  }
  return res.data?.result;
}

async function getBalance(chain, address) {
  const hex = await rpcCall(chain, 'eth_getBalance', [address, 'latest']);
  return parseInt(hex, 16) / 1e18;
}

async function getTxCount(chain, address) {
  const hex = await rpcCall(chain, 'eth_getTransactionCount', [address, 'latest']);
  return parseInt(hex, 16);
}

async function getCode(chain, address) {
  return rpcCall(chain, 'eth_getCode', [address, 'latest']);
}

async function getBlockNumber(chain) {
  const hex = await rpcCall(chain, 'eth_blockNumber', []);
  return parseInt(hex, 16);
}

// --- Analysis -----------------------------------------------------------------

async function gatherOnchainData(address, chain) {
  const [balance, txCount, code, blockNumber] = await Promise.all([
    getBalance(chain, address),
    getTxCount(chain, address),
    getCode(chain, address),
    getBlockNumber(chain),
  ]);

  const isContract = code && code !== '0x';

  return {
    address,
    chain,
    balance: balance.toFixed(6),
    nativeCurrency: chain === 'base' ? 'ETH (Base)' : 'ETH',
    txCount,
    isContract,
    codeSize: isContract ? Math.floor((code.length - 2) / 2) : 0,
    currentBlock: blockNumber,
  };
}

async function generateReport(data, depth) {
  if (!OPENAI_API_KEY) {
    // Fallback: basic text report without AI
    return formatBasicReport(data);
  }

  const res = await jsonRequest('https://api.openai.com/v1/chat/completions', {
    model: 'gpt-4o',
    messages: [
      {
        role: 'system',
        content: `You are an onchain analyst writing a research report. Use markdown formatting. Be factual and concise. Depth level: ${depth}.`,
      },
      {
        role: 'user',
        content: `Write a ${depth} onchain research report for this address. Data:\n\n${JSON.stringify(data, null, 2)}\n\nInclude: summary, key metrics, address type analysis, and any observations. If it's a contract, note the bytecode size and potential type.`,
      },
    ],
    temperature: 0.3,
    max_tokens: 2048,
  }, {
    'Authorization': `Bearer ${OPENAI_API_KEY}`,
  });

  if (res.status !== 200) {
    return formatBasicReport(data);
  }

  return res.data.choices?.[0]?.message?.content || formatBasicReport(data);
}

function formatBasicReport(data) {
  return `# Onchain Research Report

## Address: \`${data.address}\`

| Metric | Value |
|--------|-------|
| Chain | ${data.chain} |
| Balance | ${data.balance} ${data.nativeCurrency} |
| Transaction Count | ${data.txCount} |
| Type | ${data.isContract ? 'Contract' : 'EOA (Externally Owned Account)'} |
${data.isContract ? `| Bytecode Size | ${data.codeSize} bytes |` : ''}
| Current Block | ${data.currentBlock} |

---
*Report generated by LittleLobster at ${new Date().toISOString()}*
`;
}

// --- Main handler -------------------------------------------------------------

async function execute(params = {}) {
  const {
    address,
    chain = 'base',
    depth = 'standard',
    outDir = '/tmp/littlelobster',
  } = params;

  if (!address) throw new Error('Missing required param: address');
  if (!/^0x[a-fA-F0-9]{40}$/.test(address)) {
    throw new Error('Invalid address format (expected 0x + 40 hex chars)');
  }

  fs.mkdirSync(outDir, { recursive: true });

  // Gather on-chain data
  const data = await gatherOnchainData(address, chain);

  // Generate report
  const report = await generateReport(data, depth);

  // Save
  const timestamp = Date.now();
  const shortAddr = address.slice(0, 8);
  const filename = `research-${shortAddr}-${timestamp}.md`;
  const filepath = path.join(outDir, filename);

  fs.writeFileSync(filepath, report, 'utf-8');

  return {
    filepath,
    filename,
    chain,
    address,
    isContract: data.isContract,
    balance: data.balance,
    txCount: data.txCount,
    depth,
  };
}

module.exports = { execute };
